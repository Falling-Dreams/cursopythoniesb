## Praticando 1

lista = ["João", 18, "Python", "Maria", 20, "C"]

# mostre na tela a linguagem Python
print(lista[2])

# usando SLICE mostre na tela ["Maria", 20]
print(lista[3:5])

# usando SLICE e STEPS mostre na tela ["João", "Maria"]
print(lista[::3])

# usando um índice negativo mostre na tela 18
print(lista[-5])