#5. Crie uma calculadora capaz de fazer as operacoes de soma, subtracao, 
#multiplicacao, divisao e potencia entre dois numeros.

a = int(input("Insira um valor para o primeiro numero: "))
b = int(input("Insira um valor para o segundo numero: "))

#Soma
print("Soma: ", a + b)

#Subtracao
print("Subtracao: ", a - b)

#Multiplicacao
print("Multiplicacao: ", a * b)

#Divisao
print("Divisao: ", a / b)

#Potencia
print("Potencia a^b: ", a**b)